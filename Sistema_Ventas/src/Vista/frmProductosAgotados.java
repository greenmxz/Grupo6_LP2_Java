/*lookandfeel */
    public void deleteRows(){
        modelo= (DefaultTableModel)tablaProductos.getModel();
        int rowCount = modelo.getRowCount();
        for (int i=rowCount-1;i>=0;i--){
            modelo.removeRow(i);
        }
    }
